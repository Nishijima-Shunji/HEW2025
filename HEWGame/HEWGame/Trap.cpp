std::vector<std::vector<int>> Trap::Update(std::vector<std::vector<int>> MapDate)
{
    Map = MapDate;

    // 待機アニメーション
    SetUV(animcount % 4 , (animcount / 4) % 2);
    if (framecount % 5 == 0) {
        animcount++;
    }

    framecount++;

    return Map;
}

